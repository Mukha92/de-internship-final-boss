-- ----------------------------------------------------------------------------
-- Таблица: customer_features_mart - матрица признаков клиентов
-- Назначение: Хранение 30 бинарных признаков для ML-кластеризации
-- ----------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS mart.customer_features_mart (
    -- Идентификаторы
    customer_id         String,           -- Естественный ключ клиента (cus-XXXX)
    customer_sk         UInt32,           -- Суррогатный ключ клиента

    -- === БИНАРНЫЕ ПРИЗНАКИ (30 штук) ===

    -- Категория: product_preference (предпочтения по продуктам)
    bought_milk_last_30d    UInt8,        -- Покупал молочные продукты за последние 30 дней
    bought_fruits_last_14d  UInt8,        -- Покупал фрукты и ягоды за последние 14 дней
    not_bought_veggies_14d  UInt8,        -- Не покупал овощи и зелень за последние 14 дней
    organic_preference      UInt8,        -- Купил хотя бы 1 органический продукт
    buys_bakery             UInt8,        -- Покупал хлеб/выпечку хотя бы раз
    bought_meat_last_week   UInt8,        -- Покупал мясо/рыбу/яйца за последнюю неделю
    fruit_lover             UInt8,        -- >=3 покупок фруктов за 30 дней
    vegetarian_profile      UInt8,        -- Не купил ни одного мясного продукта за 90 дней

    -- Категория: purchase_behavior (покупательское поведение)
    recurrent_buyer         UInt8,        -- Делал более 2 покупок за последние 30 дней
    inactive_14_30          UInt8,        -- Не покупал 14-30 дней (ушедший клиент?)
    delivery_user           UInt8,        -- Пользовался доставкой хотя бы раз
    no_purchases            UInt8,        -- Не совершал ни одной покупки (только регистрация)

    -- Категория: loyalty (лояльность)
    new_customer            UInt8,        -- Зарегистрировался менее 30 дней назад
    loyal_customer          UInt8,        -- Лояльный клиент (карта и >=3 покупки)

    -- Категория: spending (платёжеспособность)
    bulk_buyer              UInt8,        -- Средняя корзина > 1000₽
    low_cost_buyer          UInt8,        -- Средняя корзина < 200₽
    recent_high_spender     UInt8,        -- Купил на сумму >2000₽ за последние 7 дней

    -- Категория: temporal (временные паттерны)
    night_shopper           UInt8,        -- Делал покупки после 20:00
    morning_shopper         UInt8,        -- Делал покупки до 10:00
    weekend_shopper         UInt8,        -- Делал >= 60% покупок в выходные
    weekday_shopper         UInt8,        -- Делал >= 60% покупок в будни
    early_bird              UInt8,        -- Покупка в промежутке между 12 и 15 часами дня

    -- Категория: payment (способы оплаты)
    prefers_cash            UInt8,        -- Оплачивал наличными >= 70% покупок
    prefers_card            UInt8,        -- Оплачивал картой >= 70% покупок

    -- Категория: location (география покупок)
    multicity_buyer         UInt8,        -- Делал покупки в разных городах
    store_loyal             UInt8,        -- Ходит только в один магазин
    switching_store         UInt8,        -- Ходит в разные магазины

    -- Категория: basket (характеристики корзины)
    single_item_buyer       UInt8,        -- >=50% покупок - 1 товар в корзине
    varied_shopper          UInt8,        -- Покупал >=4 разных категорий продуктов
    family_shopper          UInt8,        -- Среднее кол-во позиций в корзине >=4

    -- === ДОПОЛНИТЕЛЬНЫЕ МЕТРИКИ (для аналитики) ===
    total_purchases         UInt32,       -- Общее количество покупок
    total_spent             Decimal(14, 2), -- Общая сумма покупок
    avg_cart_amount         Decimal(10, 2), -- Средняя сумма корзины

    -- Служебные поля
    created_at              DateTime DEFAULT now()
)
ENGINE = ReplacingMergeTree(created_at)
ORDER BY (customer_sk, customer_id)
PRIMARY KEY (customer_sk)
SETTINGS index_granularity = 8192
COMMENT 'Матрица бинарных признаков клиентов для кластеризации';


